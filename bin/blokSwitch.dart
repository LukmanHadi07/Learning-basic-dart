/*

 Membuat blok swiftCase di dart dengan studi kasus kelulusan




 */

void main() {
  var Kelulusan = 'B';

  switch (Kelulusan) {
    case 'A':
      print('Anda dinyatakan lulus dengan grade A');
      break;
    case 'B':
      print('Anda dinyatakan lulus dengan grade B');
      break;
    case 'C':
      print('Anda dinyatakan lulus dengan grade C');
      break;
    case 'D':
      print('Anda dinyatakan lulus dengan grade D');
      break;
    default:
  }
}
