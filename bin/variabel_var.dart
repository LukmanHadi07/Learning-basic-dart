void main(List<String> args) {
  /*  var adalah sebuah keyword variabel default pada ddrt terdapat
      banyak keyword variabel yang dimiliki oleh bahasa dart salah satunya a
      adalah var . sedangkan namaDepan adalah sebuah nama variabel yang menyimpan 
      value ( nilai ) , type data pada dart tidak perlu di deklarasi.
      Karena dart otomatis akan membaca type data ketka kita menulis nilai tersebut 
      didalam variabel.

      exmple :
      
      var (-> sebagai keyword variabel ) namaDepan ( -> sebagai variabel yang menyimpan value)
      
      = ' Lukman Hadi ' (-> value yang ditulis bertype datakan string karena ditulis menggunakan
      tanda petik '')

      print(namaDepan) -> Untuk menampilkan value di layar

      untuk penggunaan variabel var nilai didalam variabel bisa dirubah ketika 
      kita ingin merubah nilai tersebut seperti :

      example namaDepan = 'Flutter didepan'; 
      variabel namaDepan awalnya mempunyai value(nilai) = 'Lukman Hadi';
      nah untuk penggunaan var kita bisa merubah value tersebut 

   */
  var namaDepan = 'Lukman Hadi';

  namaDepan = 'Flutter didepan'; // namaDepan value yang telah dirubah
  print(namaDepan);
}
