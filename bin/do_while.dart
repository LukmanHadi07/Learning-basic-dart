void main() {
  var Counter = 100;

  do {
    print('Perulangan ke $Counter');
    Counter++;
  } while (Counter <= 10);
}


/* 

  Perulangan do while 
   Perulangan ini yang di eksekusi duluan adalah yang statementnya yaitu do
   sedangkan perulangannya di eksekusi di akhir


   example :
     
   do {
    statement 
   }  while ( parameter )


